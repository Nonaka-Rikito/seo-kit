import { createCommandHandler } from '../lib/command-handler.js';

export function register(app) {
  app.command('/seo-top100', createCommandHandler({
    label: 'オーガニックTop100レポート',
    buildPrompt: () => [
      'あなたは /organic-top100 コマンドを実行します。',
      '`.claude/commands/organic-top100.md` の手順に従って実行してください。',
      '',
      '【重要: 出力ルール】',
      'output/ への保存と同時に、レポートの全文を標準出力してください。',
      'MDファイルに書き込む内容と完全に同一の全文を標準出力すること。',
      '以下の全セクションを省略せず出力:',
      '- サマリー',
      '- 🔴 要対応記事（原因分析・クエリ別数値・推奨アクション含む）',
      '- ⚠️ 注意記事一覧',
      '- Top100 全記事一覧テーブル',
      '- データ信頼度',
      'サマリーのみ・要約のみの出力は不可。',
      '',
      '【重要: 根拠データの明記ルール】',
      '🔴要対応記事の原因分析・推奨アクションには、必ず以下を含めること:',
      '- 参考にした競合ページの実際のURL',
      '- 競合ページの実際のタイトル・H2構成',
      '- どのツール（GSC/Ahrefs/Clarity）のどの数値に基づく判断かを「※ データソース:」で明記',
      '- 推奨アクションの「根拠」欄に、参考競合URLと参照データを必ず記載',
      '根拠なし・URL未記載の提案は禁止。',
    ].join('\n'),
  }));
}
